export type I18nKey = keyof typeof messages.en;

export function t(language: Language, key: I18nKey, values: Record<string, string | number> = {}): string {
  let template: string = messages[language][key] ?? messages.en[key] ?? key;
  for (const [name, value] of Object.entries(values)) {
    template = template.replaceAll(`{${name}}`, String(value));
  }
  return template;
}

export function getDefaultLanguage(locale = globalThis.navigator?.language ?? "en"): Language {
  return locale.toLowerCase().startsWith("zh") ? "zh" : "en";
}
